function print_problem_descriptions()

file = fopen( '~/Dropbox/papers/sbq-paper/tables/integrands_auto.tex', 'w');

% Print all the usual table header stuff
fprintf(file, '%% --- Automatically generated by print_problem_descriptions.m ---\n');
fprintf(file, '%% Exported at %s\n', datestr(now()));
fprintf(file, '\\begin{table}[h!]\n');
%fprintf(file, '\\caption{{\\small\n');
%fprintf(file, '\n');
%fprintf(file, '}}\n');
fprintf(file, '\\label{tbl:problem_descriptions}\n');
fprintf(file, '\\begin{center}\n');
fprintf(file, '\\begin{tabular}{ll}\n');

fprintf(file, ' D & Problem Description \\ \n')

fprintf(file, ' \\\\ \\midrule \n');   % Using booktabs

problems = define_integration_problems();
num_problems = length(problems);

for p_ix = 1:num_problems
    problem = problems{p_ix};
    fprintf(file, '%d & %s \\\\ \n', problem.dimension, problem.description );
end


fprintf(file, '\\end{tabular}\n');
fprintf(file, '\\end{center}\n');
fprintf(file, '\\end{table}\n');
fprintf(file, '%% End automatically generated LaTeX\n');
fclose(file);
